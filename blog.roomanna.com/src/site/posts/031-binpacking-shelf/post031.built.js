define('common-packing',[], function initPacking() {
  function pow2(input) {
    return Math.pow(2, Math.ceil(Math.log2(input)));
  };

  function max(items, accessor) {
    var i,
        value,
        max = 0;
    for (i = 0; i < items.length; i++) {
      value = accessor(items[i]);
      if (value > max) { max = value; }
    }
    return max;
  };

  function percent(ratio) {
    return Math.round(ratio * 10000) / 100;
  };

  function Packing(width, height) {
    this.items = [];
    this.width = width;
    this.height = height;
    this.metrics = {};
  };

  Packing.prototype.add = function add(x, y, width, height, data) {
    this.items.push({
      x: x,
      y: y,
      width: width,
      height: height,
      data: data
    });
  };

  Packing.prototype.extendHeight = function extendHeight() {
    this.height = pow2(max(this.items, function(i) { return i.y + i.height; }));
  };

  Packing.prototype.extendWidth = function extendHeight() {
    this.width = pow2(max(this.items, function(i) { return i.x + i.width; }));
  };

  Packing.prototype.calculate = function calculate() {
    var i,
        filledWidth,
        filledHeight,
        filledArea,
        itemArea,
        item;
    this.metrics.canvasArea = this.width * this.height;
    this.metrics.itemArea = 0;
    this.metrics.filledArea = 0;
    this.metrics.overflowedArea = 0;
    this.metrics.firstClipped = 0;
    for (i = 0; i < this.items.length; i++) {
      item = this.items[i];
      itemArea = item.width * item.height;
      filledWidth = Math.max(0, Math.min(item.width, this.width - item.x));
      filledHeight = Math.max(0, Math.min(item.height, this.height - item.y));
      filledArea = filledWidth * filledHeight;
      if (itemArea != filledArea && this.metrics.firstClipped === 0) {
        this.metrics.firstClipped = i;
      }
      this.metrics.itemArea += itemArea;
      this.metrics.filledArea += filledArea;
      this.metrics.overflowedArea += (itemArea - filledArea);
    }
    this.metrics.filledRatio = this.metrics.filledArea / this.metrics.canvasArea;
    this.metrics.overflowedRatio = this.metrics.overflowedArea / this.metrics.itemArea;
    this.metrics.filledPercent = percent(this.metrics.filledRatio);
    this.metrics.overflowedPercent = percent(this.metrics.overflowedRatio);
  };

  return Packing;
});


define('common-shelf',[], function initShelf() {
  function Shelf(maxWidth) {
    this.x = 0;
    this.y = 0;
    this.height = 0;
    this.maxWidth = maxWidth;
    this.isOpen = true;
  };

  Shelf.prototype.fitsX = function fitsX(wordWidth) {
    return this.x + wordWidth <= this.maxWidth;
  };

  Shelf.prototype.fitsY = function fitsY(wordHeight) {
    return this.height >= wordHeight;
  }

  Shelf.prototype.remainingX = function remainingX() {
    return this.maxWidth - this.x;
  };

  Shelf.prototype.canAdd = function canAdd(word) {
    if (!this.fitsX(word.width)) {
      return false;
    }
    // Only allow adding words which exceed height if the shelf is open.
    if (!this.isOpen && !this.fitsY(word.height)) {
      return false;
    }
    return true;
  };

  Shelf.prototype.add = function add(word) {
    if (this.height < word.height) {
      this.height = word.height;
    }
    this.x += word.width;
  };

  // Returns the next shelf.  Now height of this shelf cannot be extended.
  Shelf.prototype.close = function close() {
    var shelf = new Shelf(this.maxWidth);
    shelf.y = this.y + this.height;
    this.isOpen = false;
    return shelf;
  };

  return Shelf;
});

define('algorithm-shelfnf',['common-packing', 'common-shelf'], function initShelfNextFit(Packing, Shelf) {

  function pack(controls) {
    var i,
        word,
        shelf = new Shelf(controls.config.width),
        packing = new Packing(controls.config.width, controls.config.height);
    for (i = 0; i < controls.words.length; i++) {
      word = controls.words[i];
      if (!shelf.canAdd(word)) {
        shelf = shelf.close();
      }
      packing.add(shelf.x, shelf.y, word.width, word.height, word);
      shelf.add(word);
    }
    return packing;
  };

  return {
    pack: pack
  };
});

define('algorithm-shelfff',['common-packing', 'common-shelf'], function initShelfFirstFit(Packing, Shelf) {

  function pack(controls) {
    var i,
        j,
        word,
        shelf,
        placed,
        shelves = [ new Shelf(controls.config.width) ],
        packing = new Packing(controls.config.width, controls.config.height);
    for (i = 0; i < controls.words.length; i++) {
      placed = false;
      word = controls.words[i];
      for (j = 0; j < shelves.length; j++) {
        shelf = shelves[j];
        if (!placed && shelf.canAdd(word)) {
          packing.add(shelf.x, shelf.y, word.width, word.height, word);
          shelf.add(word);
          placed = true;
        }
      }
      if (!placed) {
        shelf = shelves[shelves.length-1].close();
        shelves.push(shelf);
        packing.add(shelf.x, shelf.y, word.width, word.height, word);
        shelf.add(word);
      }
    }
    return packing;
  };

  return {
    pack: pack
  };
});


define('common-shelfbestfit',['common-packing', 'common-shelf'], function initShelfBestFit(Packing, Shelf) {

  function pack(controls, heuristic, compare) {
    var i,
        j,
        word,
        score,
        shelf,
        bestScore,
        bestShelf,
        shelves = [ new Shelf(controls.config.width) ],
        packing = new Packing(controls.config.width, controls.config.height);
    for (i = 0; i < controls.words.length; i++) {
      word = controls.words[i];
      bestScore = false;
      bestShelf = -1;
      for (j = 0; j < shelves.length; j++) {
        shelf = shelves[j];
        if (shelf.canAdd(word)) {
          score = heuristic(shelf, word);
          if (bestScore === false || compare(score, bestScore)) {
            bestScore = score;
            bestShelf = j;
          }
        }
      }
      if (bestShelf === -1) {
        shelf = shelves[shelves.length-1].close();
        shelves.push(shelf);
        bestShelf = shelves.length - 1;
      }
      shelf = shelves[bestShelf];
      packing.add(shelf.x, shelf.y, word.width, word.height, word);
      shelf.add(word);
    }
    return packing;
  };

  return {
    pack: pack
  };
});


define('algorithm-shelfbwf',['common-shelfbestfit'], function initShelfBestWidthFit(ShelfBestFit) {
  function heuristic(shelf, word) {
    return shelf.remainingX() - word.width; // Score is leftover X space.
  };

  function compare(score, bestScore) {
    return score < bestScore; // Lower is better.
  };

  return {
    pack: function pack(controls) {
      return ShelfBestFit.pack(controls, heuristic, compare);
    }
  };
});

define('algorithm-shelfbhf',['common-shelfbestfit'], function initShelfBestHeightFit(ShelfBestFit) {
  function heuristic(shelf, word) {
    return shelf.height - word.height; // Score is leftover Y space.
  };

  function compare(score, bestScore) {
    return score < bestScore; // Lower is better.
  };

  return {
    pack: function pack(controls) {
      return ShelfBestFit.pack(controls, heuristic, compare);
    }
  };
});

define('algorithm-shelfbaf',['common-shelfbestfit'], function initShelfBestAreaFit(ShelfBestFit) {
  function heuristic(shelf, word) {
    var shelfArea = shelf.remainingX() * shelf.height,
        wordArea = word.width * word.height;
    return shelfArea - wordArea; // Score is leftover area.
  };

  function compare(score, bestScore) {
    return score < bestScore; // Lower is better.
  };

  return {
    pack: function pack(controls) {
      return ShelfBestFit.pack(controls, heuristic, compare);
    }
  };
});



define('algorithm-shelfwwf',['common-shelfbestfit'], function initShelfWorstWidthFit(ShelfBestFit) {
  function heuristic(shelf, word) {
    if (shelf.remainingX() === word.width) {
      return Number.MAX_VALUE; // Immediately pick this fit.
    }
    return shelf.remainingX() - word.width; // Score is leftover X space.
  };

  function compare(score, bestScore) {
    return score > bestScore; // Higher is better.
  };

  return {
    pack: function pack(controls) {
      return ShelfBestFit.pack(controls, heuristic, compare);
    }
  };
});

